package com.system.model;

import java.util.List;

public class Menu {
	private Long menuId;
	private String menuName;
	private Long menuPosition;
	private String menuUrl;
	private Long menuParent;
	private List<Menu> child; 
	
	public Long getMenuId() {
		return menuId;
	}
	public void setMenuId(Long menuId) {
		this.menuId = menuId;
	}
	public String getMenuName() {
		return menuName;
	}
	public void setMenuName(String menuName) {
		this.menuName = menuName;
	}
	public Long getMenuPosition() {
		return menuPosition;
	}
	public void setMenuPosition(Long menuPosition) {
		this.menuPosition = menuPosition;
	}
	public String getMenuUrl() {
		return menuUrl;
	}
	public void setMenuUrl(String menuUrl) {
		this.menuUrl = menuUrl;
	}
	public Long getMenuParent() {
		return menuParent;
	}
	public void setMenuParent(Long menuParent) {
		this.menuParent = menuParent;
	}

	public List<Menu> getChild() {
		return child;
	}
	public void setChild(List<Menu> child) {
		this.child = child;
	}
	@Override
	public String toString() {
		return "Menu [menuId=" + menuId + ", menuName=" + menuName
				+ ", menuPosition=" + menuPosition + ", menuUrl=" + menuUrl
				+ ", menuParent=" + menuParent + ", child=" + child + "]";
	}

}
